import java.util.*;

public class Main {
    public static void main(String[] args) {
      // swap two numbers 
      
      int a = 1;
      int b = 2; 
      
      System.out.println("Before swapping: ") ; 
      
      System.out.println(a + " "+ b);
      
      int c = a; 
      a = b ; 
      b = c ; 
      
      System.out.println("After swapping: ") ; 
      
      System.out.println(a + " "+ b);
      
  }
}